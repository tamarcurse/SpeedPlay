    public void GetObjectFromServer(InfoFromServer infoFromServer) {
        jAns1Bth.setVisible(true);
        jAns2Bth.setVisible(true);
        jAns3Bth_.setVisible(true);
        jAns4Bth_.setVisible(true);
        jMessegelbl.setVisible(false);
        jWinnerLbl.setVisible(true);
        jquestuonTextPane.setVisible(true);
        switch (infoFromServer.getTypeInfo()) {
            case question:
                setQuestion(infoFromServer.getInfo());
                break;
//            case players:
//                setPlayers(infoFromServer.getInfo());
            case winner:
                setWinner(infoFromServer.getInfo());
                break;
            case endPlay:
                FinishPlayFram fram = new FinishPlayFram();
                fram.setWinner(winner, jnamePlayerLbl.getText());
                fram.setVisible(true);
                this.setVisible(false);
        }
    }

