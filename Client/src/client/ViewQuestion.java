    private void CheckAns(int ansId) {
        
        int x = question.getNumRight();
        if (ansId == question.getNumRight()) {
            JOptionPane.showMessageDialog(ViewQuestion.this, "תשובה נכונה" + "נוספו לך חמש נקודות", "שים לב", JOptionPane.INFORMATION_MESSAGE);
            player.addPoint(5);
        } else {
            JOptionPane.showMessageDialog(ViewQuestion.this, "תשובה שגויה, ירדו לך עשר נקודות", "שים לב", JOptionPane.INFORMATION_MESSAGE);
            player.subPoint(10);
        }
        jpointsLbl.setText(String.valueOf(player.getPoints()));
        jWinnerLbl.setVisible(true);
    }
    
